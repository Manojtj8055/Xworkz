package practice.xworkz.app;

public class ConditionalStatements {

	public static void main(String[] args) {
		   
		
		int x=17;
		int y=14;
		int z=13;
		
		
		
		if(x>y && y>z) {
		System.out.println(x);
		System.out.println(z);
		}
		else {
		System.out.println(y);
		System.out.println(z);
		}
		
	}
}
